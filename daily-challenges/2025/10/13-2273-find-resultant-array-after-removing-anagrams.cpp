/* Approach 1 (Naive): */
class Solution {
  private:
    bool anagrams(string word1, string word2) {
        sort(all(word1));
        sort(all(word2));

        return word1 == word2;
    }

  public:
    vector<string> removeAnagrams(vector<string> &words) {
        vector<string> result = words;
        int n = sz(result);
        int i = 1;

        while (i < n) {
            if (anagrams(result[i - 1], result[i]))
                result.erase(begin(result) + i);
            else
                i++;

            n = sz(result);
        }

        return result;
    }
};

int main() {
    Solution obj;
    vector<string> v = {"abba", "baba", "bbaa", "cd", "cd"};
    dump(obj.removeAnagrams(v));
    v = {"a", "b", "c", "d", "e"};
    dump(obj.removeAnagrams(v));
}

