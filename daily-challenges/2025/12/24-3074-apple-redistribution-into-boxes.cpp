/* Approach 1 (Sorting): */
class Solution {
  public:
    int minimumBoxes(vector<int> &apple, vector<int> &capacity) {
        int apples = accumulate(all(apple), 0);
        sort(all(capacity), greater<int>());

        int boxesCount = 0;
        iterate(it, capacity) {
            if (apples <= 0)
                break;

            apples -= it;
            boxesCount++;
        }

        return boxesCount;
    }
};

int main() {
    Solution obj;
    vi a = {1, 3, 2};
    vi c = {4, 3, 1, 5, 2};
    println(obj.minimumBoxes(a, c));
    a = {5, 5, 5};
    c = {2, 4, 2, 7};
    println(obj.minimumBoxes(a, c));
}

