/* Approach 2 (Euclidean Algorithm): */
class Solution {
  public:
    int countOperations(int num1, int num2) {
        int count = 0;
        while (num1 && num2) {
            count += num1 / num2;
            num1 %= num2;
            swap(num1, num2);
        }

        return count;
    }
};

int main() {
    Solution obj;
    println(obj.countOperations(2, 3));
    println(obj.countOperations(10, 10));
}

