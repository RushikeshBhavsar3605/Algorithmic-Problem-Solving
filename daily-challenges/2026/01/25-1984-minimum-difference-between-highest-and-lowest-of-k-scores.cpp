/* Approach 1 (Naive): */
class Solution {
  public:
    int minimumDifference(vector<int> &nums, int k) {
        sort(all(nums));
        int mini = INT_MAX;

        for (int left = 0; left + k - 1 < sz(nums); left++) {
            int right = left + k - 1;
            updateMin(mini, nums[right] - nums[left]);
        }

        return mini;
    }
};

int main() {
    Solution obj;
    vi v = {90};
    println(obj.minimumDifference(v, 1));
    v = {9, 4, 1, 7};
    println(obj.minimumDifference(v, 2));
}

