/* Approach 1 (Bit Manipulation): */
class Solution {
  public:
    bool hasAlternatingBits(int n) {
        // init: store the least significant bit as current bit
        bool currBit = n & 1;
        // update: remove the least significant bit
        n >>= 1;

        // loop: traverse remaining bits until number becomes 0
        while (n != 0) {
            // condition: if current bit is same as previous bit → not alternating
            if (currBit == (n & 1))
                return false;

            // update: move to next bit
            currBit = n & 1;
            // update: shift right to process next bit
            n >>= 1;
        }

        // result: all adjacent bits were alternating
        return true;
    }
};

int main() {
    Solution obj;
    cout << obj.hasAlternatingBits(5) << endl;
    cout << obj.hasAlternatingBits(7) << endl;
    cout << obj.hasAlternatingBits(11) << endl;
    return 0;
}

