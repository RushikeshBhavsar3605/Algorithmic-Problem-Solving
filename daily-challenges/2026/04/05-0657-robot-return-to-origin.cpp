/* Approach 1 (Naive): */
class Solution {
  public:
    bool judgeCircle(string moves) {
        int horizontal = 0;
        int vertical = 0;

        iterate(it, moves) {
            if (it == 'U')
                vertical++;
            if (it == 'D')
                vertical--;
            if (it == 'L')
                horizontal--;
            if (it == 'R')
                horizontal++;
        }

        return horizontal == 0 && vertical == 0;
    }
};

int main() {
    Solution obj;
    auto res = obj.judgeCircle("UD");
    print(res);
    res = obj.judgeCircle("LL");
    print(res);
    return 0;
}

